public class GaltoLit {
    //program to convert gallons to liters
    public static void main(String[] args) {

        double gallons;
        double liters;

        //assign values
        gallons = 10;
        liters = 3.78541 * gallons;         //conversion

        System.out.println(gallons+" Gallons is "+liters+" Liters.");

    }
}
